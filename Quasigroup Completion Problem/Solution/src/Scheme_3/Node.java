package Scheme_3;

public class Node {
    public int parenti;
    public int parentj;
    public int indexi;
    public int indexj;

    public Node(int parenti, int parentj, int indexi, int indexj) {
        this.parenti = parenti;
        this.parentj = parentj;
        this.indexi = indexi;
        this.indexj = indexj;
    }
}
